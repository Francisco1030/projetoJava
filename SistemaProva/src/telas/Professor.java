    public void logar() {
        if (txtLoginProf.getText().equals("")) {
            JOptionPane.showMessageDialog(null, "Digite o Login.");
            txtLoginProf.grabFocus();
        } else {

            GetSetUsuario log = new GetSetUsuario();

            log.setLogin(txtLoginProf.getText());
            log.setSenha(passSenhaProf.getText());

            String usuario = (txtLoginProf.getText());
            String Senha = (passSenhaProf.getText());

            //String login = (loginfield.getText());
            //    String senha =  (senhafield.getPassword().toString());
            Connection Con = (Connection) new Conexao().getConnection();

            String slq = "select * from professor where loginProf = ? and senhaProf = ?";
            try {
                PreparedStatement stm = (PreparedStatement) Con.prepareStatement(slq);
                stm.setString(1, usuario);
                stm.setString(2, Senha);
                ResultSet rs = (ResultSet) stm.executeQuery();
                System.out.println(usuario);
                System.out.println(Senha);
                if (rs.next()) {
                    //int nivel = rs.getInt("Nivel");
                    JOptionPane.showMessageDialog(null, "Bem Vindo " + usuario + "!");
                    new TelaProfessor().show();
                    dispose();

                } else {

                    JOptionPane.showMessageDialog(null, "Usu√°rio ou senha incorretos.");
                }
            } catch (Exception e) {
                JOptionPane.showMessageDialog(null, "Erro ao conectar com o Banco de Dados.");
            }
        }

    }

