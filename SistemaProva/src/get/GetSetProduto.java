public class GetSetProduto {
    
    private String codReferencia;
    private String descricao;
    private String precoProduto;
    private String precoVenda;
    private String estoqueCritico;
    private String estoqueAtual;
    private String marca;

    /**
     * @return the codReferencia
     */
    public String getCodReferencia() {
        return codReferencia;
    }

    /**
     * @param codReferencia the codReferencia to set
     */
    public void setCodReferencia(String codReferencia) {
        this.codReferencia = codReferencia;
    }

    /**
     * @return the descricao
     */
    public String getDescricao() {
        return descricao;
    }

    /**
     * @param descricao the descricao to set
     */
    public void setDescricao(String descricao) {
        this.descricao = descricao;
    }

    /**
     * @return the precoProduto
     */
    public String getPrecoProduto() {
        return precoProduto;
    }

    /**
     * @param precoProduto the precoProduto to set
     */
    public void setPrecoProduto(String precoProduto) {
        this.precoProduto = precoProduto;
    }

    /**
     * @return the precoVenda
     */
    public String getPrecoVenda() {
        return precoVenda;
    }

    /**
     * @param precoVenda the precoVenda to set
     */
    public void setPrecoVenda(String precoVenda) {
        this.precoVenda = precoVenda;
    }

    /**
     * @return the estoqueCritico
     */
    public String getEstoqueCritico() {
        return estoqueCritico;
    }

    /**
     * @param estoqueCritico the estoqueCritico to set
     */
    public void setEstoqueCritico(String estoqueCritico) {
        this.estoqueCritico = estoqueCritico;
    }

    /**
     * @return the estoqueAtual
     */
    public String getEstoqueAtual() {
        return estoqueAtual;
    }

    /**
     * @param estoqueAtual the estoqueAtual to set
     */
    public void setEstoqueAtual(String estoqueAtual) {
        this.estoqueAtual = estoqueAtual;
    }

    /**
     * @return the marca
     */
    public String getMarca() {
        return marca;
    }

    /**
     * @param marca the marca to set
     */
    public void setMarca(String marca) {
        this.marca = marca;
    }
 
}
