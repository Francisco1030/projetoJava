
package get;

/**
 *
 * @author Aluno
 */
public class GetSetProfessor {
    
private String nomeProf;
private String loginProf;
private String senhaProf;
private String areaProf;

    /**
     * @return the nomeProf
     */
    public String getNomeProf() {
        return nomeProf;
    }

    /**
     * @param nomeProf the nomeProf to set
     */
    public void setNomeProf(String nomeProf) {
        this.nomeProf = nomeProf;
    }

    /**
     * @return the loginProf
     */
    public String getLoginProf() {
        return loginProf;
    }

    /**
     * @param loginProf the loginProf to set
     */
    public void setLoginProf(String loginProf) {
        this.loginProf = loginProf;
    }

    /**
     * @return the senhaProf
     */
    public String getSenhaProf() {
        return senhaProf;
    }

    /**
     * @param senhaProf the senhaProf to set
     */
    public void setSenhaProf(String senhaProf) {
        this.senhaProf = senhaProf;
    }

    /**
     * @return the areaProf
     */
    public String getAreaProf() {
        return areaProf;
    }

    /**
     * @param areaProf the areaProf to set
     */
    public void setAreaProf(String areaProf) {
        this.areaProf = areaProf;
    }

    
}
